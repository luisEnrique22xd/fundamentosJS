//operadore Matematicos 
// + - * / % **
// += -= *= /= %= **=
console.log(10 + 10);
console.log(10 - 10);
console.log(10 * 10);
console.log(10 / 10);
console.log(10 % 10); //Modulo
console.log(10 ** 10); //Potencia

let num = 15;
console.log(num += 5);
console.log(num -= 5);
console.log(num *= 5);
console.log(num /= 5);
console.log(num %= 5);

//operadores booleanos
// > < >= <= !== ===
console.log(1>2);
console.log(1<2);
console.log(1>=2);
console.log(1<=2);
console.log(1!==2);
console.log(1===2);

//operadores logicos 
// and &&
console.log(true && true);
// or || 
console.log(true || false);
